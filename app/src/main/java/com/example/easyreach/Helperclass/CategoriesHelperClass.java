package com.example.easyreach.Helperclass;

public class CategoriesHelperClass {

    int cc_image;
    String cc_title;

    public CategoriesHelperClass( int cc_image, String cc_title) {

        this.cc_image = cc_image;
        this.cc_title = cc_title;
    }



    public int getCc_image() {
        return cc_image;
    }

    public String getCc_title() {
        return cc_title;
    }


}
